Week 4 Day 22
On self Discipline as Freedom

Self-discipline is a form of freedom, freedom from laziness and lethargy, freedom from weakness and fear or doubt. It allows you to feel inner strength, your talent. He is master of, rather than a slave to, his own thoughts and emotions.
If you think of self-discipline as in terms of deprivation and suffering, you'll never find enjoyment in personal growth, and most likely will soon give up on your endeavors.
The freedom to be master of your thoughts and emotions is ultimately worth more than any temporary gratification.